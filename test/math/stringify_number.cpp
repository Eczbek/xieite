static_assert(xte::approx_equal(xte::parse_number<double>(xte::stringify_number(7.0, 3.5), 3.5), 7.0));
static_assert(xte::approx_equal(xte::parse_number<double>(xte::stringify_number(10.0, 3.5), 3.5), 10.0));
static_assert(xte::approx_equal(xte::parse_number<double>(xte::stringify_number(100.0, 7.5), 7.5), 100.0));
static_assert(xte::approx_equal(xte::parse_number<double>(xte::stringify_number(1.5, 1.5), 1.5), 1.5));
static_assert(xte::approx_equal(xte::parse_number<double>(xte::stringify_number(1.0, 0.5), 0.5), 1.0));
static_assert(xte::approx_equal(xte::parse_number<double>(xte::stringify_number(123.0, 0.5), 0.5), 123.0));
