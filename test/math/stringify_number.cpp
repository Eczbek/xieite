static_assert(xte::stringify_number(16.0625, 16.0) == "10.1");
static_assert(xte::stringify_number(5.5, 2.0) == "101.1");
static_assert(xte::stringify_number(2.75, 2.0) == "10.11");
static_assert(xte::stringify_number(10.5, -10.0) == "191.5");
static_assert(xte::stringify_number(-10.5, -10.0) == "10.5");
static_assert(xte::stringify_number(8.25, -10.0) == "9.85");
static_assert(xte::stringify_number(5.0, -2.0) == "101.0");
static_assert(xte::stringify_number(-5.0, -2.0) == "1111.0");
static_assert(xte::stringify_number(6.5, -2.0) == "11011.1");
static_assert(xte::stringify_number(0.5, 0.5) == "10.0");
static_assert(xte::stringify_number(1.0, std::numbers::phi) == "1.0");
static_assert(xte::stringify_number(42.0, std::numbers::phi) == "10100010.00100001");
static_assert(xte::stringify_number(123.456, 10.0, {}, 0) == "123.");
static_assert(xte::parse_number<int>(xte::stringify_number(123)) == 123);
static_assert(xte::parse_number<int>(xte::stringify_number(-123)) == -123);
static_assert(xte::parse_number<int>(xte::stringify_number(0)) == 0);
static_assert(xte::parse_number<int>(xte::stringify_number(255, 16), 16) == 255);
static_assert(xte::parse_number<int>(xte::stringify_number(13, 2), 2) == 13);
static_assert(xte::parse_number<int>(xte::stringify_number(63, 8), 8) == 63);
static_assert(xte::parse_number<int>(xte::stringify_number(3, 1), 1) == 3);
static_assert(xte::parse_number<int>(xte::stringify_number(5, 1), 1) == 5);
static_assert(xte::parse_number<int>(xte::stringify_number(3, -1), -1) == 3);
static_assert(xte::parse_number<int>(xte::stringify_number(1, -1), -1) == 1);
static_assert(xte::parse_number<int>(xte::stringify_number(2, -1), -1) == 2);
static_assert(xte::parse_number<int>(xte::stringify_number(10, -10), -10) == 10);
static_assert(xte::parse_number<int>(xte::stringify_number(-10, -10), -10) == -10);
static_assert(xte::parse_number<int>(xte::stringify_number(8, -10), -10) == 8);
static_assert(xte::parse_number<int>(xte::stringify_number(-8, -10), -10) == -8);
static_assert(xte::parse_number<unsigned>(xte::stringify_number(0xDEADBEEF, 16u), 16u) == 0xDEADBEEF);
