#include <xte/math/approx_equal.hpp>
#include <xte/math/parse_number.hpp>
#include <xte/math/stringify_number.hpp>
#include <numbers>

static_assert(xte::stringify_number(123) == "123");
static_assert(xte::stringify_number(-123) == "-123");
static_assert(xte::stringify_number(0) == "0");
static_assert(xte::stringify_number(1) == "1");
static_assert(xte::stringify_number(-1) == "-1");
static_assert(xte::stringify_number(0xFF, 16) == "FF");
static_assert(xte::stringify_number(0b1101, 2) == "1101");
static_assert(xte::stringify_number(0765, 8) == "765");
static_assert(xte::stringify_number(255, 16) == "FF");
static_assert(xte::stringify_number(10, 16) == "A");
static_assert(xte::stringify_number(3, 1) == "111");
static_assert(xte::stringify_number(5, 1) == "11111");
static_assert(xte::stringify_number(-3, 1) == "-111");
static_assert(xte::stringify_number(3, -1) == "10101");
static_assert(xte::stringify_number(1, -1) == "1");
static_assert(xte::stringify_number(2, -1) == "101");
static_assert(xte::stringify_number(-3, -1) == "-10101");
static_assert(xte::stringify_number(10, -10) == "190");
static_assert(xte::stringify_number(-10, -10) == "10");
static_assert(xte::stringify_number(8, -10) == "8");
static_assert(xte::stringify_number(-8, -10) == "12");
static_assert(xte::stringify_number(0) == "0");
static_assert(xte::stringify_number(0, 2) == "0");
static_assert(xte::stringify_number(0, 16) == "0");
static_assert(xte::stringify_number(123, 0) == "0");
static_assert(xte::stringify_number(123.456, 0.0) == "0.0");
static_assert(xte::stringify_number(123.456) == "123.456");
static_assert(xte::stringify_number(0.5) == "0.5");
